CREATE OR REPLACE FUNCTION MZONTUL.CLASS_AVG(E_YEAR IN NVARCHAR2,TERM IN SMALLINT,
                          LECTURE_CODE IN NVARCHAR2)
RETURN NUMBER IS
C_AVG NUMBER(5,2);
BEGIN 
  
  SELECT AVG(T.AVERAGE)INTO C_AVG
  FROM MZONTUL.TRANSCRIPT T
  WHERE T.E_YEAR=E_YEAR AND T.TERM=TERM AND T.LECTURE_CODE=LECTURE_CODE;
  
  RETURN C_AVG;
END;

SELECT MZONTUL.CLASS_AVG('2009-2010',1,'SENG101') AS C_AVG FROM DUAL;