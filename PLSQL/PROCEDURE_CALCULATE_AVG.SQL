CREATE OR REPLACE PROCEDURE CALCULATE_AVG(E_YEAR IN NVARCHAR2,SEMESTER IN SMALLINT,
                          LECTURE_CODE IN NVARCHAR2)AS
BEGIN
  UPDATE TRANSCRIPT T
  SET AVERAGE=MIDTERM*0.6+FINAL*0.4
  WHERE T.EDUCATION_YEAR=E_YEAR AND T.SEMESTER=SEMESTER AND T.LECTURE_CODE=LECTURE_CODE;
END;

